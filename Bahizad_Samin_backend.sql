  Contractor_ID INT NOT NULL,
  Constuction_Type VARCHAR(80) NOT NULL,
  Average_Price VARCHAR(30) ,
  Office_ID INT,
  Phone_Num CHAR(12),
  PRIMARY KEY (Contractor_ID)
);

CREATE TABLE House_Tour_Appointment (
  Appt_ID INT NOT NULL,
  Property_ID INT NOT NULL,
  Date DATE,
  Time TIME,
  Client_ID INT ,
  Agent_ID INT ,
  PRIMARY KEY (Appt_ID)
);


CREATE TABLE Property_Type (
  Property_TypeID INT NOT NULL,
  Property_Type VARCHAR(20)
  PRIMARY KEY (Property_TypeID)
);


CREATE TABLE Agents (
  AgentID INT NOT NULL,
  First_Name VARCHAR(8)	,
  Last_Name VARCHAR(10),
  Phone_Num CHAR(12),
  Office_ID INT,
  Email VARCHAR(50),
  PRIMARY KEY (AgentID)
);



CREATE TABLE Properties (
  Property_ID INT NOT NULL,
  Square_feet INT,
  Property_TypeID INT ,
  Property_StatusID INT ,
  Year_Built NUMERIC,
  Price_ID INT,
  Property_Addr VARCHAR(50),
  bedrooms INT,
  bathrooms INT,
  Location_ID INT NOT NULL,
  AgentID INT,
  PRIMARY KEY (Property_ID)
);

CREATE TABLE Lenders (
  Lender_ID INT NOT NULL,
  First_Name VARCHAR(8)	,
  Last_Name VARCHAR(10),
  Phone_Num CHAR(12),
  Office_ID  INT,
  Email VARCHAR(50),
  PRIMARY KEY (Lender_ID)
);

CREATE TABLE School_District(
  School_DistrictID VARCHAR(4),
  Location_ID INT NOT NULL,
  SD_Name  VARCHAR(50),
  PRIMARY KEY (School_DistrictID)
);

CREATE TABLE Client_Type (
  Client_Type_ID INT NOT NULL,
  Client_Type VARCHAR(12)
  PRIMARY KEY (Client_Type_ID)
);

